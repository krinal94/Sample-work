var grade=prompt("Please enter your Grade");

switch(grade)
{
    case "A+":
                alert("Excellent!");
                break;
    case "A":
                alert("Good Job!");
                break;
    case "A-":
                alert("Good.Keep it up!");
                break;
    case "B":
                alert("Average.Can do better!");
                break;
    default:
                alert("Opps!");
}